// ************************* WHILE Loop **************************
let index = 0
while (index <= 10) {
    console.log(`Index is ${index}`);
    index = index+1
}

// Printing values of Array..
let myArray = ["aman", 10, 12.22, "raj"]
let idx = 0
while(idx < myArray.length){
    console.log(`Index value of ${idx} is ${myArray[idx]}`);
    idx++
}
